package com.eunsungi;

public class PrimitiveDataTypes {
    public static void main(String[] args) {
        // numbers
        byte theByte = -128;
        short theShort = 32_000;
        int theInt = 787_834_533;
        long theLong = 43_442_453_453_453L;

        // decimals
        float pi = 3.14F;         // 6~7
        double doublePi = 3.1415; // 15

        // boolean
        boolean isAdult = true;

        // single character
        char nameInitial = 'A';

        System.out.println(theShort);
        System.out.println(theLong);

    }
}